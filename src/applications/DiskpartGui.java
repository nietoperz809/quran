    public DiskpartGui()
    {
        initComponents();
        Runnable r = () ->
        {
            while (true)
            {
                try
                {
                    while (inp.available() > 0)                    
                    {
                        char c = (char)inp.read();
                        outField.append(""+c);
                        outField.setCaretPosition(outField.getDocument().getLength());
                    }
                    Thread.sleep(100);
                }
                catch (Exception ex)
                {
                    System.out.println("bye");
                    return;
                }
            }
        };
        reader = new Thread (r);
        reader.start();
    }

