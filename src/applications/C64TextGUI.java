    private void createView (int x, int y)
    {
        if (bitmapView == null)
        {
            bitmapView = new TurtleWindow(x, y);
            MainWindow.getInstance().addChild(bitmapView);
        }
    }
    
    private void removeView()
    {
        if (bitmapView != null)
        {
            MainWindow.getInstance().remove(bitmapView);
            bitmapView.dispose();
            bitmapView = null;
        }
    }

